<h4>Key Points:</h4>
<ul>
    <li><strong>Prefixes:</strong> The present tense is formed by adding prefixes to the verb stem:
        <ul>
            <li><strong>بـ</strong> (bi-) for "I" (أنا) and "He/She" (هو/هي)</li>
            <li><strong>بتـ</strong> (bit-) for "You" (إنتَ/إنتي) and "She" (هي)</li>
            <li><strong>بنـ</strong> (bin-) for "We" (إحنا)</li>
        </ul>
    </li>
    <li><strong>Dropping the "ي" (Ya):</strong> The verb stem "يكتب" (yiktib) in its dictionary form starts with the letter "ي" (ya), but in most present tense conjugations, this "ي" is dropped, leaving "كتب" (ktib). The appropriate prefix is then added:
        <ul>
            <li><strong>كتبت</strong> becomes <strong>بتكتب</strong> (bitiktib) – "You (male) write."</li>
        </ul>
    </li>
</ul>

<h3>3. General Observations</h3>
<ul>
    <li><strong>Gender and Number:</strong> Both past and present tense conjugations change based on gender (male/female) and number (singular/plural).</li>
    <li><strong>Prefixes and Suffixes:</strong> Suffixes are mainly used in the past tense, while prefixes are used in the present tense.</li>
    <li><strong>Root Consistency:</strong> The root of the verb often remains the same, with prefixes and suffixes added to convey tense, subject, and aspect.</li>
</ul>

<h3>Summary:</h3>
<p>
    <strong>Past Tense:</strong> Formed by adding specific suffixes to the verb root.<br>
    <strong>Present Tense:</strong> Formed by adding prefixes, with the "ي" (ya) often dropped from the verb stem, and different prefixes used for different subjects.<br>
    <strong>Flexibility:</strong> These rules can be applied to many other verbs in Egyptian Arabic, with slight variations based on the verb's structure.
</p>
